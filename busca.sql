	I.idINSTITUICAO = IC.INSTITUICAO_idINSTUICAO INNER JOIN CURSO AS C ON C.idCURSO = IC.CURSO_idCURSO
	WHERE C.total_cred < 200;
SELECT PROFESSORES.nome, DEPARTAMENTO.nome, PROJETOS.nome FROM PROFESSORES AS P INNER JOIN DEPARTAMENTO AS D ON
	D.idDEPARTAMENTO = P.DEPARTAMENTO_idDEPARTAMENTO INNER JOIN PROJETOS AS Q ON Q.DEPARTAMENTO_idDEPARTAMENTO = D.idDEPARTAMENTO
	WHERE Q.data_inicio > DATE('31/12/2018');
